//---------------------------------------------
//비교 연산자 : 두 값을 비교하는 연산자
//---------------------------------------------
var_dump(1 > 0);
var_dump(1 < 0);
var_dump(1 >= 0);
var_dump(1 <= 0);






$num = 1;
$str = "1";
//변수1 == 변수2 : 변수1과 변수2는 같다. 불완전 비교(데이터 타입은 체크X)
var_dump($num == $str);
//변수1 === 변수2 : 변수1과 변수2는 같다. 완전 비교(데이터 타입은 체크ㅇ)
var_dump($num === $str);
//변수! == 변수2 : 변수1과 변수2는 같지 않다. 불완전 비교(데이터 타입은 체크X)
var_dump($num != $str);
//변수1 ! === 변수2 : 변수1과 변수2는 같지 않다. 완전 비교(데이터 타입은 체크ㅇ)
var_dump($num !== $str);

//---------------------------------------------
//논리 연산자 : 값이 BOOLEAN 타입만 가지는 집합에서 사용하는 연산(and 연산자, or 연산자, ! 연산자)
var_dump(1 === 1 && 2 === 1);

//or 연산자 
var_dump(1 === 1 || 2 === 1);

//not 연산자 
var_dump(!(1 === 1));

//------------------------------------------------
//삼항 연산자 : 조건식의 결과에 따라 다른 결과를 반환 
//------------------------------------------------

$_result = 1 === 1 ? "참입니다." : "거짓입니다.";
var_dump($result);
