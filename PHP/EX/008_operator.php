//---------------------------------------------
//증감 연산자 : 1을 더하거나 뺴는 연산자 
//---------------------------------------------

//후위 증감 연산자
$num = 0;
$num++;
echo $num."\n";
$num++;
echo $num."\n";
echo "--------------\n";

//전위 증감 연산자
